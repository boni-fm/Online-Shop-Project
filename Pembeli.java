package project_001;

public class Pembeli {
    private String namaPembeli, namaJalan, kotaRumah, noTelp;
    private int kodePos, nomorRumah;

    public Pembeli() {
    }

    public String getNamaPembeli() {
        return namaPembeli;
    }

    public void setNamaPembeli(String namaPembeli) {
        this.namaPembeli = namaPembeli;
    }

    public String getNamaJalan() {
        return namaJalan;
    }

    public void setNamaJalan(String namaJalan) {
        this.namaJalan = namaJalan;
    }

    public String getKotaRumah() {
        return kotaRumah;
    }

    public void setKotaRumah(String kotaRumah) {
        this.kotaRumah = kotaRumah;
    }

    public int getNomorRumah() {
        return nomorRumah;
    }

    public void setNomorRumah(int nomorRumah) {
        this.nomorRumah = nomorRumah;
    }

    public String getNoTelp() {
        return noTelp;
    }

    public void setNoTelp(String noTelp) {
        this.noTelp = noTelp;
    }
    
    public int getKodePos() {
        return kodePos;
    }

    public void setKodePos(int kodePos) {
        this.kodePos = kodePos;
    }
    
    
}
